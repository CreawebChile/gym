document.addEventListener('DOMContentLoaded', function() {
    const newsList = document.getElementById('news-list');
    const fullArticle = document.getElementById('full-article');
    const articleContent = fullArticle.querySelector('.article-content');
    const backButton = fullArticle.querySelector('.back-button');
    const featuredArticle = document.querySelector('.featured');

    // Mobile menu elements
    const menuBtn = document.querySelector('.menu-btn');
    const navLinks = document.querySelector('.nav-links');
    const menuOverlay = document.querySelector('.menu-overlay');
    
    // Mobile menu toggle
    function toggleMenu() {
        navLinks.classList.toggle('active');
        menuOverlay.classList.toggle('active');
        document.body.style.overflow = navLinks.classList.contains('active') ? 'hidden' : '';
    }

    menuBtn.addEventListener('click', toggleMenu);
    menuOverlay.addEventListener('click', toggleMenu);

    // Close menu on link click
    navLinks.querySelectorAll('a').forEach(link => {
        link.addEventListener('click', () => {
            if (navLinks.classList.contains('active')) {
                toggleMenu();
            }
        });
    });

    // Close menu on window resize
    window.addEventListener('resize', () => {
        if (window.innerWidth > 768 && navLinks.classList.contains('active')) {
            toggleMenu();
        }
    });

    // News categories functionality
    const categories = document.querySelectorAll('.news-categories a');
    categories.forEach(category => {
        category.addEventListener('click', (e) => {
            e.preventDefault();
            categories.forEach(c => c.classList.remove('active'));
            category.classList.add('active');
            // Here you could add filtering logic based on categories
        });
    });

    // Función para mostrar el artículo completo
    function showArticle(id) {
        const article = newsData[id];
        if (!article) {
            console.error('Artículo no encontrado');
            return;
        }

        articleContent.innerHTML = `
            <h1>${article.title}</h1>
            <div class="article-meta">
                <span><i class="far fa-calendar"></i> ${article.date}</span>
                <span><i class="far fa-user"></i> ${article.author}</span>
            </div>
            <img src="${article.image}" alt="${article.title}" class="article-image">
            <div class="article-body">${article.content}</div>
        `;

        newsList.style.display = 'none';
        featuredArticle.style.display = 'none';
        fullArticle.style.display = 'block';
        window.scrollTo(0, 0);
    }

    // Event listener para los enlaces "Leer más"
    document.addEventListener('click', function(e) {
        const readMoreBtn = e.target.closest('.read-more');
        if (readMoreBtn) {
            e.preventDefault();
            const id = readMoreBtn.dataset.id;
            showArticle(id);
        }
    });

    // Event listener para el botón de volver
    backButton.addEventListener('click', function() {
        fullArticle.style.display = 'none';
        newsList.style.display = 'grid';
        featuredArticle.style.display = 'block';
    });
});
